function palette = get_palette(num_colors)
    % Get default color palette for the plot
    %
    % Args:
    %    num_colors (float): Number of colors in the palette
    % 
    % Returns:
    %    palette (float): Matrix (R, G, B) with the default color palette

    colors = [ 30,  80,  82;...
               30,  80,  82;...
               50, 116, 119;...
               50, 116, 119;...
               77, 151, 147;...
               77, 151, 147;...
              113, 184, 172;...
              113, 184, 172;...
              168, 207, 190;...
              149, 191, 176;...
              132, 175, 162;...
              121, 157, 146;...
              100, 140, 129;...
               68, 118, 108;...
               81, 143, 127;...
              100, 162, 144;...
              113, 179, 156;...
              129, 192, 169;...
              152, 210, 190;...
              186, 231, 212];
    
    % Definitions
    l = length(colors);
    x = 1:l;
    y = linspace(1, l, num_colors);

    % Interpolate between the input colors to create the palette
    palette = interp1(x, colors, y, 'linear');

    % Normalize
    palette = palette ./ 255;
end